package org.example.hw_2.task_1;

public class MinMax {
    public static void main(String[] args) {
        byte maxByte = Byte.MAX_VALUE;
        byte minByte = Byte.MIN_VALUE;
        System.out.println("maxByte = " + maxByte + "; minByte = " + minByte);

        short maxShort = Short.MAX_VALUE;
        short minShort = Short.MIN_VALUE;
        System.out.println("maxShort = " + maxShort + "; minShort = " + minShort);

        char maxChar = Character.MAX_VALUE;
        char minChar = Character.MIN_VALUE;
        System.out.println("maxChar = " + maxChar + "; minChar = " + minChar);

        int maxInt = Integer.MAX_VALUE;
        int minInt = Integer.MIN_VALUE;
        System.out.println("maxInt = " + maxInt + "; minInt = " + minInt);

        long maxLong = Long.MAX_VALUE;
        long minLong = Long.MIN_VALUE;
        System.out.println("maxLong = " + maxLong + "; minLong = " + minLong);

        float maxFloat = Float.MAX_VALUE;
        float minFloat = Float.MIN_VALUE;
        System.out.println("maxFloat = " + maxFloat + "; minFloat = " + minFloat);

        double maxDouble = Double.MAX_VALUE;
        double minDouble = Double.MIN_VALUE;
        System.out.println("maxDouble = " + maxDouble + "; minDouble = " + minDouble);
    }
}
